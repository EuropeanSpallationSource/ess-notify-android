package eu.ess.ics.android.essnotify.datamodel;

import java.util.List;

/**
 * Wrapper around user data that can be used to check if user needs to log in.
 */
public class User {

    private int id;
    private String username;
    private List<String> apn_tokens;
    private boolean is_active;
    private boolean is_admin;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public List<String> getApn_tokens() {
        return apn_tokens;
    }

    public void setApn_tokens(List<String> apn_tokens) {
        this.apn_tokens = apn_tokens;
    }

    public boolean isIs_active() {
        return is_active;
    }

    public void setIs_active(boolean is_active) {
        this.is_active = is_active;
    }

    public boolean isIs_admin() {
        return is_admin;
    }

    public void setIs_admin(boolean is_admin) {
        this.is_admin = is_admin;
    }
}
